package com.class03;

public class CompoundOperator {

	public static void main(String[] args) {
		
		int num=100;
		num+=500;
		System.out.println(num);
		num-=67;
		System.out.println(num);
		double cakePiece=11;
		cakePiece/=4;
		System.out.println(cakePiece);
		double cake;
		cake=25;
		cake%=7;
		System.out.println(cake);
		

	}

}
